import { v2 as cloudinary } from "cloudinary";

const cloudName = process.env.NEXT_PUBLIC_CLOUDINARY_CLOUD_NAME;
const apiKey = process.env.CLOUDINARY_API_KEY;
const apiSecret = process.env.CLOUDINARY_API_SECRET;

if (!cloudName || !apiKey || !apiSecret) {
  console.warn(`
⚠️  Cloudinary configuration missing

Required environment variables:
  NEXT_PUBLIC_CLOUDINARY_CLOUD_NAME
  CLOUDINARY_API_KEY
  CLOUDINARY_API_SECRET

Get these from: https://cloudinary.com/console
  `);
}

cloudinary.config({ cloud_name: cloudName, api_key: apiKey, api_secret: apiSecret, secure: true });

export { cloudinary };

export function generateSignature(paramsToSign: Record<string, string>) {
  return cloudinary.utils.api_sign_request(paramsToSign, apiSecret || "");
}

export function getImageUrl(
  publicId: string,
  options: { width?: number; height?: number; crop?: string; quality?: string | number } = {}
): string {
  const { width, height, crop = "fill", quality = "auto" } = options;
  return cloudinary.url(publicId, { transformation: [{ width, height, crop, quality, fetch_format: "auto" }] });
}

export async function deleteImage(publicId: string): Promise<boolean> {
  try {
    const result = await cloudinary.uploader.destroy(publicId);
    return result.result === "ok";
  } catch { return false; }
}

