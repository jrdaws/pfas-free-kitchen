export function getMockProducts(filters?: { 
  category?: string; 
  tier?: number[]; 
  material?: string[];
  brand?: string;
}) {
  let products = [...MOCK_PRODUCTS];
  
  if (filters?.category) {
    products = products.filter(p => 
      p.category.path.some(c => c.toLowerCase().includes(filters.category!.toLowerCase())) ||
      p.category.name.toLowerCase().includes(filters.category!.toLowerCase()) ||
      p.category.id.includes(filters.category!)
    );
  }
  
  if (filters?.tier?.length) {
    products = products.filter(p => filters.tier!.includes(p.verification.tier));
  }
  
  if (filters?.material?.length) {
    products = products.filter(p => 
      filters.material!.some(m => 
        p.material_summary.toLowerCase().includes(m.toLowerCase())
      )
    );
  }
  
  if (filters?.brand) {
    products = products.filter(p => 
      p.brand.slug === filters.brand || p.brand.id === filters.brand
    );
  }
  
  return products;
}

