export function getMockFacets() {
  const tierCounts: Record<number, number> = { 0: 0, 1: 0, 2: 0, 3: 0, 4: 0 };
  const materialCounts: Record<string, number> = {};
  const brandCounts: Record<string, number> = {};
  
  for (const product of MOCK_PRODUCTS) {
    tierCounts[product.verification.tier]++;
    
    const material = product.material_summary.toLowerCase();
    if (material.includes('stainless')) {
      materialCounts['stainless-steel'] = (materialCounts['stainless-steel'] || 0) + 1;
    }
    if (material.includes('cast iron') || material.includes('enameled')) {
      materialCounts['cast-iron'] = (materialCounts['cast-iron'] || 0) + 1;
    }
    if (material.includes('carbon steel') || material.includes('black steel')) {
      materialCounts['carbon-steel'] = (materialCounts['carbon-steel'] || 0) + 1;
    }
    
    brandCounts[product.brand.slug] = (brandCounts[product.brand.slug] || 0) + 1;
  }
  
  return {
    tier: [
      { value: 4, label: 'Monitored', count: tierCounts[4] },
      { value: 3, label: 'Lab Tested', count: tierCounts[3] },
      { value: 2, label: 'Policy Reviewed', count: tierCounts[2] },
      { value: 1, label: 'Brand Statement', count: tierCounts[1] },
      { value: 0, label: 'Unknown', count: tierCounts[0] },
    ],
    material: [
      { value: 'stainless-steel', label: 'Stainless Steel', count: materialCounts['stainless-steel'] || 0 },
      { value: 'cast-iron', label: 'Cast Iron', count: materialCounts['cast-iron'] || 0 },
      { value: 'carbon-steel', label: 'Carbon Steel', count: materialCounts['carbon-steel'] || 0 },
    ],
    brand: Object.entries(brandCounts).map(([slug, count]) => {
      const brand = MOCK_BRANDS.find(b => b.slug === slug);
      return { value: slug, label: brand?.name || slug, count };
    }).sort((a, b) => b.count - a.count),
  };
}

